package com.keymanager.monitoring.vo;

public class IndustryDetailVO {

    private String industryName;

    private String website;

    private String qq;

    private String telephone;

    private Integer weight;

    private String identifyCustomer;

    private String remark;

    public String getIndustryName() {
        return industryName;
    }

    public void setIndustryName(String industryName) {
        this.industryName = industryName;
    }

    public String getWebsite() {
        return website;
    }

    public void setWebsite(String website) {
        this.website = website;
    }

    public String getQq() {
        return qq;
    }

    public void setQq(String qq) {
        this.qq = qq;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public Integer getWeight() {
        return weight;
    }

    public void setWeight(Integer weight) {
        this.weight = weight;
    }

    public String getIdentifyCustomer() {
        return identifyCustomer;
    }

    public void setIdentifyCustomer(String identifyCustomer) {
        this.identifyCustomer = identifyCustomer;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}
